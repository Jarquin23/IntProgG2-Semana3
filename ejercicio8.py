dolar = input("Ingrese la cantidad en dólares ")
dolar = float(dolar)
valor_euro = 1.10
valor_libra = 1.31
valor_yenes = 0.0068
euro = dolar * valor_euro
libra = dolar * valor_libra
yenes = dolar * valor_yenes
print("La cantidad en dólares es de: ", dolar)
print("El equivalente en euros es: ", euro)
print("El equivalente en libras es: ", libra)
print("El equivalente en yenes es de: ", yenes)